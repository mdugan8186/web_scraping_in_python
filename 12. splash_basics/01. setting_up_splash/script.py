# ==== setting up splash with docker ====

# == after docker is installed install splash ==
# 1. in the terminal:
# docker pull scrapinghub/splash


# == run splash in a browser ==
# 1. in the terminal:
# docker run -it -p 8050:8050 scrapinghub/splash

# 2. once it is completed running look for the message:
# Server listening on http:0.0.0.0:8050

# 3. copy the link and paste it in the browser:
# http:0.0.0.0:8050


