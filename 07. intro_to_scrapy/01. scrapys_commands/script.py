# ==== scrapy's commands ====

# == getting commands ==

# to see scrapy commands type scapy into the terminal. the below text will pop up in the terminal

# Scrapy 2.12.0 - no active project

# Usage:
#   scrapy <command> [options] [args]

# Available commands:
#   bench         Run quick benchmark test
#   fetch         Fetch a URL using the Scrapy downloader
#   genspider     Generate new spider using pre-defined templates
#   runspider     Run a self-contained spider (without creating a project)
#   settings      Get settings values
#   shell         Interactive scraping console
#   startproject  Create new project
#   version       Print Scrapy version
#   view          Open URL in browser, as seen by Scrapy

#   [ more ]      More commands available when run from project directory

# Use "scrapy <command> -h" to see more info about a command

# == run a command ==
# to run a command type scapy and then the command into the terminal
# for more info about the command use -h at the end of the command

# run the command
# ex. scrapy bench

# more info about the command
# ex. scrapy bench -h
